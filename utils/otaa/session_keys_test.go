package otaa

import (
	"testing"

	"github.com/TheThingsNetwork/ttn/core/types"
	. "github.com/smartystreets/assertions"
)

func TestCalculateSessionKeys(t *testing.T) {
	a := New(t)

	// MSB first
	appKey := types.AppKey{0xBE, 0xC4, 0x99, 0xC6, 0x9E, 0x9C, 0x93, 0x9E, 0x41, 0x3B, 0x66, 0x39, 0x61, 0x63, 0x6C, 0x61}
	devNonce := [2]byte{0x73, 0x69}
	netID := [3]byte{0x00, 0x00, 0x00}
	appNonce := [3]byte{0xAE, 0x3B, 0x1C}

	appSKey, nwkSKey, err := CalculateSessionKeys(appKey, appNonce, netID, devNonce)
	a.So(err, ShouldBeNil)

	// MSB first
	expectedNwkSKey := types.NwkSKey{0x33, 0xD5, 0xF3, 0x74, 0x29, 0xDA, 0x60, 0xF0, 0xA5, 0x7A, 0xB5, 0xAA, 0x06, 0x95, 0xE4, 0x98}
	expectedAppSKey := types.AppSKey{0x71, 0x4F, 0xA5, 0x53, 0x03, 0x07, 0xD6, 0x03, 0xE8, 0x7C, 0x78, 0x65, 0xDF, 0x86, 0x2A, 0x85}

	a.So(appSKey, ShouldResemble, expectedAppSKey)
	a.So(nwkSKey, ShouldResemble, expectedNwkSKey)
}
